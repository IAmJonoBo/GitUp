const toFile = (path: string, content: string, language?: string): GeneratedFile => ({
  path,
  content,
  language,
});

const normalizeName = (name: string) =>
  name
    .toLowerCase()
    .replace(/[^a-z0-9-_]+/g, "-")
    .replace(/^-+|-+$/g, "") || "my-repo";

const normalizeNodeVersion = (value?: string | null) => {
  if (!value) return null;
  const trimmed = value.trim();
  if (!trimmed) return null;
  const match = trimmed.match(/\d+(?:\.\d+){0,2}/);
  return match ? match[0] : null;
};

const resolveNodeVersion = (state: WizardState) =>
  normalizeNodeVersion(state.techStack.nodeVersion) || RECOMMENDED_NODE_VERSION;

const buildReadme = (state: WizardState) => {
  const title = state.projectDetails.name || "New Repository";
  const description =
    state.projectDetails.description || "A bootstrapped repository generated by GitUp.";
  return `# ${title}\n\n${description}\n`;
};

const buildLicense = (license: string) => `TODO: Add the ${license} license text here.\n`;

const buildSecurity = () => `# Security Policy\n\nPlease report security issues privately.\n`;

const buildChangelog = () => `# Changelog\n\n## [Unreleased]\n- Initial scaffold\n`;

const buildEditorConfig = () =>
  `root = true\n\n[*]\ncharset = utf-8\nindent_style = space\nindent_size = 2\nend_of_line = lf\ninsert_final_newline = true\ntrim_trailing_whitespace = true\n`;

const buildPrettierConfig = () =>
  JSON.stringify(
    {
      printWidth: 100,
      singleQuote: false,
      trailingComma: "all",
    },
    null,
    2,
  ) + "\n";

const buildIssueTemplate = (title: string) =>
  `# ${title}\n\n## Summary\n\n## Steps to Reproduce\n\n## Expected Behavior\n\n## Actual Behavior\n`;

const buildPullRequestTemplate = () =>
  `## Summary\n\n## Testing\n\n## Checklist\n- [ ] Tests added/updated\n- [ ] Docs updated\n`;

const buildContributing = () =>
  `# Contributing\n\nThanks for contributing! Please open an issue before major changes.\n`;

const buildCodeOfConduct = () => `# Code of Conduct\n\nBe respectful and inclusive.\n`;

const buildDependabot = (ecosystem: string) =>
  `version: 2\nupdates:\n  - package-ecosystem: "${ecosystem}"\n    directory: "/"\n    schedule:\n      interval: "weekly"\n`;

const buildRuffConfig = () => `line-length = 100\nselect = ["E", "F", "I"]\n`;

const buildNvmrc = (nodeVersion: string) => `${nodeVersion}\n`;

const buildDockerignore = () =>
  [
    "node_modules",
    "dist",
    "build",
    "coverage",
    ".git",
    ".github",
    ".vscode",
    ".DS_Store",
    "__pycache__",
    "*.pyc",
    "target",
  ].join("\n") + "\n";

const buildDockerfile = (state: WizardState, nodeVersion: string) => {
  switch (state.techStack.language) {
    case Language.PYTHON:
      return `FROM python:3.12-slim\nWORKDIR /app\nCOPY . .\nRUN python -m pip install --upgrade pip\nRUN if [ -f requirements.txt ]; then pip install -r requirements.txt; fi\nCMD ["python", "main.py"]\n`;
    case Language.GO:
      return `FROM golang:1.22-alpine AS builder\nWORKDIR /app\nCOPY . .\nRUN go build -o app .\n\nFROM alpine:3.19\nWORKDIR /app\nCOPY --from=builder /app/app ./app\nCMD ["./app"]\n`;
    case Language.RUST: {
      const binaryName = normalizeName(state.projectDetails.name);
      return `FROM rust:1 AS builder\nWORKDIR /app\nCOPY . .\nRUN cargo build --release\n\nFROM debian:bookworm-slim\nWORKDIR /app\nCOPY --from=builder /app/target/release/${binaryName} ./app\nCMD ["./app"]\n`;
    }
    default: {
      const pm = state.techStack.packageManager || "npm";
      const usesCorepack = pm === "pnpm" || pm === "yarn";
      const installCmd =
        pm === "pnpm"
          ? "pnpm install --frozen-lockfile"
          : pm === "yarn"
            ? "yarn install --frozen-lockfile"
            : pm === "bun"
              ? "bun install"
              : "npm install";
      const entrypoint =
        state.techStack.language === Language.JAVASCRIPT ? "src/index.js" : "src/index.ts";
      const bunSetup = pm === "bun" ? "RUN npm install -g bun\n" : "";
      const corepack = usesCorepack ? "RUN corepack enable\n" : "";
      return `FROM node:${nodeVersion}-alpine\nWORKDIR /app\nCOPY . .\n${corepack}${bunSetup}RUN ${installCmd}\nCMD ["node", "${entrypoint}"]\n`;
    }
  }
};

const buildHuskyPreCommit = (state: WizardState) => {
  const lines = ["#!/bin/sh", "set -e"];
  const commands: string[] = [];
  const pm = state.techStack.packageManager || "npm";

  if (state.techStack.language === Language.PYTHON) {
    if (state.automation.linting) commands.push("python -m ruff check .");
    if (state.automation.tests) commands.push("python -m pytest");
  } else if (state.techStack.language === Language.GO) {
    if (state.automation.linting) commands.push("go vet ./...");
    if (state.automation.tests) commands.push("go test ./...");
  } else if (state.techStack.language === Language.RUST) {
    if (state.automation.linting) commands.push("cargo clippy -- -D warnings");
    if (state.automation.tests) commands.push("cargo test");
  } else {
    if (state.automation.linting) commands.push(`${pm} run lint`);
    if (state.automation.tests) commands.push(`${pm} run test`);
  }

  if (commands.length === 0) {
    commands.push('echo "No pre-commit checks configured"');
  }

  return `${lines.concat(commands).join("\n")}\n`;
};

const buildCiWorkflow = (nodeVersion: string, installCmd: string, runCmd: string[]) => {
  const steps = [
    "      - uses: actions/checkout@v4",
    "      - uses: actions/setup-node@v4",
    `        with:\n          node-version: "${nodeVersion}"`,
    `      - run: ${installCmd}`,
    ...runCmd.map((cmd) => `      - run: ${cmd}`),
  ];

  return `name: CI\non:\n  push:\n  pull_request:\n${buildWorkflowConcurrency("ci")}jobs:\n  build:\n    runs-on: ubuntu-latest\n    permissions:\n      contents: read\n    steps:\n${steps.join("\n")}\n`;
};

const buildReleaseWorkflow = (language: Language, nodeVersion: string) => {
  const steps = [
    "      - uses: actions/checkout@v4",
    "      - uses: actions/setup-node@v4",
    `        with:\n          node-version: "${nodeVersion}"`,
    "      - run: npm install",
    "      - run: npm run build",
    "      - run: npm pack",
  ];

  if (language === Language.PYTHON) {
    steps.push("      - uses: actions/setup-python@v5");
    steps.push('        with:\n          python-version: "3.12"');
    steps.push("      - run: python -m pip install --upgrade pip");
    steps.push("      - run: python -m pip install build");
    steps.push("      - run: python -m build");
  }

  if (language === Language.GO) {
    steps.push("      - uses: actions/setup-go@v5");
    steps.push('        with:\n          go-version: "1.22"');
    steps.push("      - run: go build ./...");
  }

  if (language === Language.RUST) {
    steps.push("      - uses: dtolnay/rust-toolchain@stable");
    steps.push("        with:\n          components: clippy,rustfmt");
    steps.push("      - run: cargo build --release");
  }

  steps.push("      - uses: softprops/action-gh-release@v2");
  steps.push("        with:\n          generate_release_notes: true");

  return `name: Release\non:\n  push:\n    tags:\n      - "v*.*.*"\n  workflow_dispatch:\n${buildWorkflowConcurrency("release")}jobs:\n  release:\n    runs-on: ubuntu-latest\n    permissions:\n      contents: write\n    steps:\n${steps.join("\n")}\n`;
};

const buildCodeqlWorkflow = (language: Language) => {
  const codeqlLang =
    language === Language.PYTHON
      ? "python"
      : language === Language.GO
        ? "go"
        : "javascript-typescript";

  return `name: CodeQL\non:\n  push:\n  pull_request:\nschedule:\n  - cron: "0 3 * * 1"\n${buildWorkflowConcurrency("codeql")}jobs:\n  analyze:\n    runs-on: ubuntu-latest\n    permissions:\n      actions: read\n      contents: read\n      security-events: write\n    steps:\n      - uses: actions/checkout@v4\n      - uses: github/codeql-action/init@v3\n        with:\n          languages: ${codeqlLang}\n      - uses: github/codeql-action/analyze@v3\n`;
};

const buildWorkflowConcurrency = (name: string) =>
  `concurrency:\n  group: "${name}-\${{ github.ref }}"\n  cancel-in-progress: true\n`;

